// Wardrobe System for Pockets
// Each slot can be customized independently

export type Rarity = 'common' | 'uncommon' | 'rare' | 'epic' | 'legendary'
export type SlotType = 'hat' | 'shirt' | 'pants' | 'shoes' | 'accessory'

export interface WardrobeItem {
  id: string
  name: string
  slot: SlotType
  rarity: Rarity
  emoji: string // For UI display
  description: string
  unlockMethod: 'default' | 'purchase' | 'achievement' | 'event' | 'level'
  price?: number // In crumbs
  requiredLevel?: number
  requiredAchievement?: string
  colors: string[] // Available color options
  zIndex: number // Layer order for rendering
}

export interface PlayerOutfit {
  hat: string | null
  shirt: string | null
  pants: string | null
  shoes: string | null
  accessory: string | null
}

